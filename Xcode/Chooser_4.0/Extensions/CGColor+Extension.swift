    static var black      = UIColor.init(hexVal: 0x000000).cgColor
    static var neon       = UIColor.init(hexVal: 0xE52C61).cgColor
    static var lithium    = UIColor.init(hexVal: 0xF34C49).cgColor
    static var helium     = UIColor.init(hexVal: 0xFF6E31).cgColor
    static var oxygen     = UIColor.init(hexVal: 0xFF9D31).cgColor
    static var iron       = UIColor.init(hexVal: 0xFFCD31).cgColor
    static var barium     = UIColor.init(hexVal: 0x81C652).cgColor
    static var chlorine   = UIColor.init(hexVal: 0x03C074).cgColor
    static var phosphorus = UIColor.init(hexVal: 0x1BA19D).cgColor
    static var silicon    = UIColor.init(hexVal: 0x3182C5).cgColor
    static var zinc       = UIColor.init(hexVal: 0x586CB8).cgColor
    static var nitrogen   = UIColor.init(hexVal: 0x7E55AB).cgColor
}
